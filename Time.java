public class Time {
    private String corDaCamisa, cidade, tamanhoTorcida, popularidade;
    
    public String getCorDaCamisa(){
        return corDaCamisa;
    }
    public void setCorDaCamisa(String corDaCamisa){
        this.corDaCamisa = corDaCamisa;
    }

    public String getCidade(){
        return cidade;
    }
    public void setCidade(String cidade){
        this.cidade = cidade;
    }

    public String getTamanhoTorcida(){
        return tamanhoTorcida;
    }
    public void setTamanhoTorcida(){
        this.tamanhoTorcida = tamanhoTorcida;
    }

    public String getPopularidade(){
        return popularidade;
    }
    public void setPopularidade(){
        this.popularidade = popularidade;
    }



}